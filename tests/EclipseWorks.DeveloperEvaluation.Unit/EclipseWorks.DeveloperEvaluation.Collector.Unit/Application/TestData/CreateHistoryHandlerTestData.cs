using Bogus;
using EclipseWorks.DeveloperEvaluation.Domain.Enums;
using EclipseWorks.DeveloperEvaluation.Application.Histories.CreateHistory;
namespace EclipseWorks.DeveloperEvaluation.Unit.Domain;

public static class CreateHistoryHandlerTestData
{
    /// <summary>
    /// Configures the Faker to generate valid Tasl entities.
    /// </summary>
    private static readonly Faker<CreateHistoryCommand> createHistoryHandlerFaker = new Faker<CreateHistoryCommand>()
         .RuleFor(t => t.TaskId, f => new Guid())
         .RuleFor(t => t.Details, f => GenerateText(100))
         //.RuleFor(t => t.DueDate, f => TimeProvider.System.GetUtcNow().AddDays(+5))
         //.RuleFor(t => t.CreatedAt, f => TimeProvider.System.GetUtcNow().AddDays(+5))
         .RuleFor(t => t.Status, f => f.PickRandom(HistoryStatus.Comment, HistoryStatus.History));

    /// <summary>
    /// Generates a text.
    /// </summary>
    public static string GenerateText(int caracter)
    {
        return new Faker().Random.String2(caracter);
    }

    /// <summary>
    /// Generates a valid History entity with randomized data.
    /// </summary>
    public static CreateHistoryCommand GenerateValidCommand()
    {
        return createHistoryHandlerFaker.Generate();
    }
}
