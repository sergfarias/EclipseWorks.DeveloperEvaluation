using Bogus;
using EclipseWorks.DeveloperEvaluation.Application.Tasks.DeleteTask;
namespace EclipseWorks.DeveloperEvaluation.Unit.Domain;

public static class DeleteTaskHandlerTestData
{
    /// <summary>
    /// Configures the Faker to generate valid Task entities.
    /// </summary>
    private static readonly Faker<DeleteTaskCommand> deleteTaskHandlerFaker = new Faker<DeleteTaskCommand>()
        .RuleFor(u => u.Id, f => new Guid());
   
    /// <summary>
    /// Generates a valid Task entity with randomized data.
    /// </summary>
    public static DeleteTaskCommand GenerateValidCommand()
    {
        return deleteTaskHandlerFaker.Generate();
    }
}
