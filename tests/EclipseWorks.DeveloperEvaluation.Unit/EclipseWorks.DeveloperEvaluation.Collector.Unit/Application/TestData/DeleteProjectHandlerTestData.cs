using Bogus;
using EclipseWorks.DeveloperEvaluation.Application.Projects.DeleteProject;
namespace EclipseWorks.DeveloperEvaluation.Unit.Domain;

public static class DeletePriojectHandlerTestData
{
    /// <summary>
    /// Configures the Faker to generate valid Project entities.
    /// </summary>
    private static readonly Faker<DeleteProjectCommand> deleteProjectHandlerFaker = new Faker<DeleteProjectCommand>()
        .RuleFor(u => u.Id, f => new Guid());

    /// <summary>
    /// Generates a valid Project entity with randomized data.
    /// </summary>
    public static DeleteProjectCommand GenerateValidCommand()
    {
        return deleteProjectHandlerFaker.Generate();
    }
}
